class Product:
    def __init__(self, name, identifier, price, baseprice, baseprice_unit, store, category, url):
        self.name = name
        self.identifier = identifier
        self.price = price
        self.baseprice = baseprice
        self.baseprice_unit = baseprice_unit
        self.store = store
        self.category = category
        self.url = url
